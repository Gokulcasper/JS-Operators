// Comparision Operator

var a = "10";
var b = 10;

console.log(a == b);
console.log(a === b);
console.log(a != b);

//Relational Operator

var a = 10;
var b = 20;

console.log("Greatest :", a > b);
console.log("lowest :", a < b);
console.log("Greater than or Equal to :", a >= b);
console.log("Lesser Than or Equal to :", a <= b);

//Logical Operator

var mark = 35;

console.log(mark >= 35 && mark <= 100);

var a = 10;

console.log(a == 5 || a == 10);

a = false;
console.log(!a);
